public class foreach {

    public static void main(String args[]){
        int num[] = {1, 2, 3, 4, 5};
        for(int var:num){
            System.out.println(var);
        }

        String str[] = {"Kamal", "Sam", "Hari"};
        for(String var:str){
            System.out.println(var);
        }
    }
}
